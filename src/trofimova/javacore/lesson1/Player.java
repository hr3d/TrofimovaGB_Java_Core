package trofimova.javacore.lesson1;

public class Player {
    private String namePlayer;
    private int power;

    public Player(String namePlayer, int power) {
        this.namePlayer = namePlayer;
        this.power = power;
    }

    public String getNamePlayer() {
        return namePlayer;
    }

    public int getPower() {
        return power;
    }
}




